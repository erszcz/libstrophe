/* (R0+R1), R2, R3, R4 are the different operations used in SHA1 */
unsafe extern "C" fn host_to_be(mut i: uint32_t) -> uint32_t {
    return (i << 24 as libc::c_int |
                i >> 32 as libc::c_int - 24 as libc::c_int) &
               0xff00ff00 as libc::c_uint |
               (i << 8 as libc::c_int |
                    i >> 32 as libc::c_int - 8 as libc::c_int) &
                   0xff00ff as libc::c_int as libc::c_uint;
    /* fallback to run-time check */
}
